public class Switch {
    public static void main(String[] args){
        int num=200;
        switch(num){
            case 100: System.out.println("case 1");
            case 200: System.out.println("case 2");
            break; // break gone stop the execution after case match the value
            case 300: System.out.println("case 3");
        }
    }
}
